import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { spawnSync } from "node:child_process";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const checkOnly = process.argv.includes("--check");
const contractPath = resolve(root, "packages/api-contract/desktop-api-v1.openapi.json");
const document = JSON.parse(readFileSync(contractPath, "utf8"));
const schemas = document.components?.schemas;

if (document.openapi !== "3.1.0" || !schemas || typeof schemas !== "object") {
  throw new Error("desktop API contract must be an OpenAPI 3.1 document with component schemas");
}

const validateNode = (value, path = "contract") => {
  if (Array.isArray(value)) {
    value.forEach((item, index) => validateNode(item, `${path}[${index}]`));
    return;
  }
  if (!value || typeof value !== "object") return;
  if (typeof value.$ref === "string" && value.$ref.startsWith("#/components/schemas/")) {
    const name = value.$ref.split("/").at(-1);
    if (!schemas[name]) throw new Error(`${path} references missing schema ${name}`);
  }
  for (const [key, child] of Object.entries(value)) validateNode(child, `${path}.${key}`);
};
validateNode(document);
for (const [name, schema] of Object.entries(schemas)) {
  if (schema.type !== "object" || !schema.properties) continue;
  for (const property of schema.required || []) {
    if (!(property in schema.properties)) throw new Error(`${name} requires missing property ${property}`);
  }
}

const words = (value) => value
  .replace(/([a-z0-9])([A-Z])/g, "$1 $2")
  .replace(/[^A-Za-z0-9]+/g, " ")
  .trim()
  .split(/\s+/)
  .filter(Boolean);
const pascal = (value) => words(value).map((word) => word[0].toUpperCase() + word.slice(1)).join("");
const snake = (value) => words(value).map((word) => word.toLowerCase()).join("_");
const refName = (schema) => schema.$ref?.split("/").at(-1);
const schemaTypes = (schema) => Array.isArray(schema.type) ? schema.type : schema.type ? [schema.type] : [];
const nullable = (schema) => schemaTypes(schema).includes("null");
const primaryType = (schema) => schemaTypes(schema).find((type) => type !== "null");

function typeScriptType(schema) {
  const referenced = refName(schema);
  if (referenced) return referenced;
  const type = primaryType(schema);
  let result;
  if (!type) result = "unknown";
  else if (type === "string") result = "string";
  else if (type === "integer" || type === "number") result = "number";
  else if (type === "boolean") result = "boolean";
  else if (type === "array") result = `${typeScriptType(schema.items || {})}[]`;
  else if (type === "object" && schema.additionalProperties && typeof schema.additionalProperties === "object") result = `Record<string, ${typeScriptType(schema.additionalProperties)}>`;
  else if (type === "object") result = "Record<string, unknown>";
  else result = "unknown";
  return nullable(schema) ? `${result} | null` : result;
}

function goType(schema, optional) {
  const referenced = refName(schema);
  let result;
  if (referenced) result = referenced;
  else {
    const type = primaryType(schema);
    if (!type) result = "any";
    else if (type === "string") result = "string";
    else if (type === "integer") result = schema.format === "int64" ? "int64" : "int";
    else if (type === "number") result = "float64";
    else if (type === "boolean") result = "bool";
    else if (type === "array") result = `[]${goType(schema.items || {}, false)}`;
    else if (type === "object" && schema.additionalProperties && typeof schema.additionalProperties === "object") result = `map[string]${goType(schema.additionalProperties, false)}`;
    else result = "map[string]any";
  }
  if ((optional || nullable(schema)) && !result.startsWith("[]") && !result.startsWith("map[") && result !== "any") return `*${result}`;
  return result;
}

function rustType(schema, optional) {
  const referenced = refName(schema);
  let result;
  if (referenced) result = referenced;
  else {
    const type = primaryType(schema);
    if (!type) result = "serde_json::Value";
    else if (type === "string") result = "String";
    else if (type === "integer") result = schema.format === "int64" ? "i64" : "i32";
    else if (type === "number") result = "f64";
    else if (type === "boolean") result = "bool";
    else if (type === "array") result = `Vec<${rustType(schema.items || {}, false)}>`;
    else if (type === "object" && schema.additionalProperties && typeof schema.additionalProperties === "object") result = `BTreeMap<String, ${rustType(schema.additionalProperties, false)}>`;
    else result = "BTreeMap<String, serde_json::Value>";
  }
  return optional || nullable(schema) ? `Option<${result}>` : result;
}

function generateTypeScript() {
  const output = ["// Generated by scripts/generate-desktop-contract.mjs. Do not edit.", ""];
  for (const [name, schema] of Object.entries(schemas)) {
    if (primaryType(schema) !== "object" || !schema.properties) continue;
    const required = new Set(schema.required || []);
    output.push(`export interface ${name} {`);
    for (const [property, propertySchema] of Object.entries(schema.properties)) {
      output.push(`  ${property}${required.has(property) ? "" : "?"}: ${typeScriptType(propertySchema)};`);
    }
    output.push("}", "");
  }
  return `${output.join("\n")}\n`;
}

function generateGo() {
  const output = ["// Code generated by scripts/generate-desktop-contract.mjs. DO NOT EDIT.", "", "package desktopcontract", ""];
  for (const [name, schema] of Object.entries(schemas)) {
    if (primaryType(schema) !== "object" || !schema.properties) continue;
    const required = new Set(schema.required || []);
    output.push(`type ${name} struct {`);
    for (const [property, propertySchema] of Object.entries(schema.properties)) {
      output.push(`\t${pascal(property)} ${goType(propertySchema, !required.has(property))} \`json:"${property}"\``);
    }
    output.push("}", "");
  }
  const source = `${output.join("\n")}\n`;
  const formatted = spawnSync("gofmt", [], { input: source, encoding: "utf8" });
  if (formatted.status !== 0) throw new Error(formatted.stderr || "gofmt failed for generated desktop contract");
  return formatted.stdout;
}

function generateRust() {
  const output = [
    "// Generated by scripts/generate-desktop-contract.mjs. Do not edit.",
    "#![allow(dead_code)]",
    "",
    "use std::collections::BTreeMap;",
    "",
    "use serde::{Deserialize, Serialize};",
    "",
  ];
  for (const [name, schema] of Object.entries(schemas)) {
    if (primaryType(schema) !== "object" || !schema.properties) continue;
    const required = new Set(schema.required || []);
    output.push("#[derive(Clone, Debug, Deserialize, Serialize, PartialEq)]", "#[serde(rename_all = \"camelCase\")]", `pub(crate) struct ${name} {`);
    for (const [property, propertySchema] of Object.entries(schema.properties)) {
      output.push(`    pub(crate) ${snake(property)}: ${rustType(propertySchema, !required.has(property))},`);
    }
    output.push("}", "");
  }
  const source = `${output.join("\n")}\n`;
  const formatted = spawnSync("rustfmt", ["--emit", "stdout", "--edition", "2021"], { input: source, encoding: "utf8" });
  if (formatted.status !== 0) throw new Error(formatted.stderr || "rustfmt failed for generated desktop contract");
  return formatted.stdout;
}

const targets = [
  ["packages/api-types/src/desktop-contract.generated.ts", generateTypeScript()],
  ["server/internal/desktopcontract/generated.go", generateGo()],
  ["packages/api-contract/generated/desktop_contract.rs", generateRust()],
];
const drift = [];
for (const [relative, generated] of targets) {
  const path = resolve(root, relative);
  let current = "";
  try {
    current = readFileSync(path, "utf8");
  } catch {
    current = "";
  }
  if (current === generated) continue;
  if (checkOnly) drift.push(relative);
  else {
    mkdirSync(dirname(path), { recursive: true });
    writeFileSync(path, generated);
  }
}

if (drift.length > 0) {
  console.error("Desktop API generated DTOs are out of date:");
  for (const relative of drift) console.error(`- ${relative}`);
  process.exit(1);
}

console.log(checkOnly ? "Desktop API contract is synchronized across Go, Rust and TypeScript." : "Generated desktop API DTOs for Go, Rust and TypeScript.");
